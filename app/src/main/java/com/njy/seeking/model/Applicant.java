public class Applicant {
    private String certificationcompany, certificationid, email,
            github, interested, lastcompany, lasteducation, linkedin, name, phone, project, website, workperiod;

    public String getCertificationcompany() {
        return certificationcompany;
    }

    public void setCertificationcompany(String certificationcompany) {
        this.certificationcompany = certificationcompany;
    }

    public String getCertificationid() {
        return certificationid;
    }

    public void setCertificationid(String certificationid) {
        this.certificationid = certificationid;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getGithub() {
        return github;
    }

    public void setGithub(String github) {
        this.github = github;
    }

    public String getInterested() {
        return interested;
    }

    public void setInterested(String interested) {
        this.interested = interested;
    }

    public String getLastcompany() {
        return lastcompany;
    }

    public void setLastcompany(String lastcompany) {
        this.lastcompany = lastcompany;
    }

    public String getLasteducation() {
        return lasteducation;
    }

    public void setLasteducation(String lasteducation) {
        this.lasteducation = lasteducation;
    }

    public String getLinkedin() {
        return linkedin;
    }

    public void setLinkedin(String linkedin) {
        this.linkedin = linkedin;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getProject() {
        return project;
    }

    public void setProject(String project) {
        this.project = project;
    }

    public String getWebsite() {
        return website;
    }

    public void setWebsite(String website) {
        this.website = website;
    }

    public String getWorkperiod() {
        return workperiod;
    }

    public void setWorkperiod(String workperiod) {
        this.workperiod = workperiod;
    }
}
